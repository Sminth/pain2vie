export interface VerseTarget {
  chapterKey: string;
  from: number;
  to: number;
}

const BOOK_NAME_TO_KEY: Record<string, BookKey> = Object.fromEntries(
  (Object.entries(BOOKS) as Array<[BookKey, { name: string }]>).map(([k, b]) => [
    b.name.toLowerCase(),
    k,
  ]),
) as Record<string, BookKey>;

/**
 * Retrouve le passage biblique d'une référence de carte ("Matthieu 5,9",
 * "1 Thessaloniciens 5,16-17", "cf. 1 Corinthiens 16,14"…).
 * Renvoie null si la citation n'est pas un verset dont on a le chapitre
 * (magistère, saints…) — la parole sera alors présentée comme une image
 * pieuse glissée dans la Bible.
 */
export function locateReference(reference: string): VerseTarget | null {
  const cleaned = reference.replace(/^cf\.\s*/i, "").trim();
  const m = cleaned.match(/^(.+?)\s+(\d+),(\d+)(?:-(\d+))?$/);
  if (!m) return null;
  const bookKey = BOOK_NAME_TO_KEY[m[1].toLowerCase()];
  if (!bookKey) return null;
  const chapter = Number(m[2]);
  const entry = Object.entries(CHAPTERS).find(
    ([, c]) => c.book === bookKey && c.chapter === chapter,
  );
  if (!entry) return null;
  const from = Number(m[3]);
  const to = m[4] ? Number(m[4]) : from;
  const hasVerses = entry[1].verses.some((v) => v.v >= from && v.v <= to);
  return hasVerses ? { chapterKey: entry[0], from, to } : null;
}
